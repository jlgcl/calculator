let add = function(num1, num2) {
    let result = num1 + num2;
    return result;
}

let subtract = function(num1, num2) {
    let result = num1 - num2;
    return result;
}

let multiply = function(num1, num2) {
    let result = num1 * num2;
    return result;
}

let divide = function(num1, num2) {
    let result = num1 / num2;
    return result;
}

let operator = function(num1, num2) {
    add(num1, num2);
    subtract(num1, num2);
    multiply(num1, num2);
    divide(num1, num2);
}